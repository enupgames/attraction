using UnityEngine;



/////////////////////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////////////////////
public static class SiLog 
{       

    ///////////////////////////////////////////////////////////////////////////////////
    //
    ///////////////////////////////////////////////////////////////////////////////////
    public static void Error(string str)
    { 
        string stack;

        Debug.Log("<color=#8B0000>ERROR</color>[" + str + "]\n"); 

        stack = UnityEngine.StackTraceUtility.ExtractStackTrace();                                
        Debug.Log("<color=#8B0000>ERROR</color>    " + stack);

    }

    ///////////////////////////////////////////////////////////////////////////////////
    //
    ///////////////////////////////////////////////////////////////////////////////////
    public static void UnsupportedIdx(int idx)
    { 
        Error("unsupported idx:" + idx + ":");
    }

    ///////////////////////////////////////////////////////////////////////////////////
    //
    ///////////////////////////////////////////////////////////////////////////////////
    public static void Message(string str)
    { 
        Debug.Log("<color=#FFFFFF>MESSAGE</color>[" + str + "]\n"); 
    }

}
